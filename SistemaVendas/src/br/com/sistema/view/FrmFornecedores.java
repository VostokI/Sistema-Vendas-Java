    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // botao editar
        CadastroVerification check = new CadastroVerification();
        Fornecedores obj = new Fornecedores();

        obj.setId(Integer.parseInt(txtcodigo.getText()));
        
        if( ! check.nomeValido(txtnome.getText())){
            return;
        }
        obj.setNome(txtnome.getText());
        
        if( ! check.cnpjEdicaoValido(txtcodigo.getText(), txtcnpj.getText())){
            return;
        }
        obj.setCnpj(txtcnpj.getText());
        
        if( ! check.emailValido(txtemail.getText())){
            return;
        }
        obj.setEmail(txtemail.getText());
        
        if( ! check.contatoValido(txtfixo.getText(), txtcel.getText())){
            return;
        }
        obj.setTelefone(txtfixo.getText());
        obj.setCelular(txtcel.getText());
        
        if( ! check.cepValido(txtcep.getText())){
            return;
        }
        obj.setCep(txtcep.getText());
       
        obj.setUf(cbuf.getSelectedItem().toString());
        
        if( ! check.cidadeValido(txtcidade.getText())){
            return;
        }
        obj.setCidade(txtcidade.getText());
        
        if( ! check.bairroValido(txtbairro.getText())){
            return;
        }
        obj.setBairro(txtbairro.getText());
        
        if( ! check.enderecoValido(txtend.getText())){
            return;
        }
        obj.setLogradouro(txtend.getText());
        
        if( ! check.numeroValido(txtnumero.getText())){
            return;
        }
        obj.setNumero(Integer.parseInt(txtnumero.getText()));

        obj.setComplemento(txtcomplemento.getText());
        obj.setUrlImagem(txtUrlImagem.getText());

        FornecedoresDAO dao = new FornecedoresDAO();

        dao.alterarFornecedor(obj);

        new InsereImagemURL().emLabel(labelFotoFornecedor, null);
        new Utilitarios().LimpaTela(painel_dados);
    }//GEN-LAST:event_jButton3ActionPerformed

