    private void txtcepKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcepKeyPressed

        //Programacao do keypress
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String cep = txtcep.getText();

            CadastroVerification check = new CadastroVerification();

            if( ! check.cepValido(cep)){
                return;
            }

            Endereco endereco = new CepVerification().buscaCepWS(cep);

            if(endereco != null){
                cbuf.setSelectedItem(endereco.getUf());
                txtcidade.setText(endereco.getCidade());
                txtbairro.setText(endereco.getBairro());
                txtend.setText(endereco.getLogradouro());

            }else{
                JOptionPane.showMessageDialog(null, "CEP inválido");      
            }
        }
    }//GEN-LAST:event_txtcepKeyPressed

    private void btnbuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscaActionPerformed
        // botao buscar cliente por nome

        new Utilitarios().LimpaTela(painel_dados);
        
        String nome = txtnome.getText();
        Fornecedores obj = new Fornecedores();
        FornecedoresDAO dao = new FornecedoresDAO();

        obj = dao.consultaPorNome(nome);

        if (obj.getNome() != null) {

            //Exibi os dados do obj nos campos de texto
            txtcodigo.setText(String.valueOf(obj.getId()));
            txtnome.setText(obj.getNome());
            txtcnpj.setText(obj.getCnpj());

            txtemail.setText(obj.getEmail());
            txtfixo.setText(obj.getTelefone());
            txtcel.setText(obj.getCelular());
            txtcep.setText(obj.getCep());
            txtend.setText(obj.getLogradouro());
            txtnumero.setText(String.valueOf(obj.getNumero()));
            txtcomplemento.setText(obj.getComplemento());
            txtbairro.setText(obj.getBairro());
            txtcidade.setText(obj.getCidade());
            cbuf.setSelectedItem(obj.getUf());
            txtUrlImagem.setText(obj.getUrlImagem());
            
            new InsereImagemURL().emLabel(labelFotoFornecedor, obj.getUrlImagem());
        } else {
            JOptionPane.showMessageDialog(null, "Fornecedor não encontrado!");
        }
    }//GEN-LAST:event_btnbuscaActionPerformed

